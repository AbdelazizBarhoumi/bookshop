let currentLocale: Locale = 'en';

export function setLocale(locale: Locale): void {
  currentLocale = locale;
  localStorage.setItem('riadh_library_locale', locale);
  document.documentElement.dir = locale === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = locale;
}

export function getLocale(): Locale {
  const stored = localStorage.getItem('riadh_library_locale') as Locale | null;
  if (stored && translations[stored]) {
    currentLocale = stored;
    return stored;
  }
  return currentLocale;
}

export function t(key: string, params?: Record<string, string | number>): string {
  let text = translations[currentLocale]?.[key] || translations.en[key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}

export function formatCurrency(amount: number): string {
  return `${Math.round(amount)} ${t('common.currency')}`;
}

